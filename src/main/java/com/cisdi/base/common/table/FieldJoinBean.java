package com.cisdi.base.common.table;

import java.io.Serializable;

/**
 * @功能说明：配合TableJoinBean
 * 			定义连接的字段名组合的集合
 * @作者：xiangjz
 * @创建日期： 2018-01-12
 */
public class FieldJoinBean implements Serializable{ 

	private static final long serialVersionUID = 1L;

	//主表字段
	private String baseField; 
	//从表字段
	private String dependField;
	
	public String getBaseField() {
		return baseField;
	}
	public void setBaseField(String baseField) {
		this.baseField = baseField;
	}
	public String getDependField() {
		return dependField;
	}
	public void setDependField(String dependField) {
		this.dependField = dependField;
	}
	public FieldJoinBean() {
		super();
	}
	public FieldJoinBean(String baseField, String dependField) {
		super();
		this.baseField = baseField;
		this.dependField = dependField;
	}

}
